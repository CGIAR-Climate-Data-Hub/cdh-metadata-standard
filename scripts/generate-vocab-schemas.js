#!/usr/bin/env node
// Generate JSON Schema enum fragments from vocab/*.json files.
//
// The vocabulary JSON files are the human-maintained source of truth. The
// generated schema fragments are used by spec/schemas/metadata-input.schema.json
// for editor and CI validation.

import { mkdir, readFile, writeFile } from "node:fs/promises";
import { existsSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");

const VOCABS = {
  domain: {
    source: resolve(ROOT, "vocab/domain.json"),
    target: resolve(ROOT, "spec/schemas/vocab/domain.schema.json"),
    title: "CDH Domain Vocabulary",
  },
  commodity: {
    source: resolve(ROOT, "vocab/commodity.json"),
    target: resolve(ROOT, "spec/schemas/vocab/commodity.schema.json"),
    title: "CDH Commodity Vocabulary",
  },
  hazard: {
    source: resolve(ROOT, "vocab/hazard.json"),
    target: resolve(ROOT, "spec/schemas/vocab/hazard.schema.json"),
    title: "CDH Hazard Vocabulary",
  },
};

async function conceptIds(path) {
  const data = JSON.parse(await readFile(path, "utf-8"));
  const ids = (data.concepts ?? [])
    .filter((c) => "id" in c)
    .map((c) => c.id);

  if (ids.length === 0) {
    throw new Error(`No concepts[].id values found in ${path}`);
  }
  const seen = new Set();
  const duplicates = new Set();
  for (const id of ids) {
    if (seen.has(id)) duplicates.add(id);
    seen.add(id);
  }
  if (duplicates.size > 0) {
    throw new Error(
      `Duplicate concept ids in ${path}: ${[...duplicates].sort().join(", ")}`,
    );
  }
  return ids;
}

function schemaFor(name, ids, title) {
  return {
    $schema: "https://json-schema.org/draft/2020-12/schema",
    $id: `https://cgiar.org/cdh/schemas/vocab/${name}.schema.json`,
    title,
    description:
      `Generated from vocab/${name}.json. Do not edit this file directly; ` +
      "update the JSON vocabulary and regenerate schemas.",
    type: "string",
    enum: ids,
  };
}

function render(schema) {
  return JSON.stringify(schema, null, 2) + "\n";
}

async function main() {
  const check = process.argv.includes("--check");
  const changed = [];

  for (const [name, cfg] of Object.entries(VOCABS)) {
    const output = render(
      schemaFor(name, await conceptIds(cfg.source), cfg.title),
    );

    if (check) {
      const current = existsSync(cfg.target)
        ? await readFile(cfg.target, "utf-8")
        : null;
      if (current !== output) changed.push(cfg.target);
      continue;
    }

    await mkdir(dirname(cfg.target), { recursive: true });
    await writeFile(cfg.target, output, "utf-8");
  }

  if (changed.length > 0) {
    for (const path of changed) {
      console.error(`out of date: ${path.replace(ROOT + "/", "")}`);
    }
    console.error("Run: npm run gen-schemas");
    process.exit(1);
  }
}

main().catch((err) => {
  console.error(err.message ?? err);
  process.exit(1);
});
